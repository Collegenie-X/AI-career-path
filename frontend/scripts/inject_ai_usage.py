def main():
    with open(PATH, encoding="utf-8") as f:
        data = json.load(f)

    inserted = 0
    skipped = 0
    for roadmap in data:
        for item in roadmap.get("items", []):
            for goal in item.get("subItems", []):
                gid = goal.get("id")
                if gid not in AI_BY_GOAL:
                    continue
                comments = goal.setdefault("comments", [])
                if any(c.get("kind") == "ai" for c in comments):
                    skipped += 1
                    continue

                # 삽입 위치: 첫 'status' 코멘트 바로 앞(없으면 끝). 시각은 인접 코멘트를 따른다.
                status_idx = next((i for i, c in enumerate(comments) if c.get("kind") == "status"), None)
                if status_idx is not None:
                    created_at = comments[status_idx].get("createdAt")
                    pos = status_idx
                elif comments:
                    created_at = comments[-1].get("createdAt")
                    pos = len(comments)
                else:
                    created_at = roadmap.get("sharedAt")
                    pos = 0

                ai_comment = {
                    "id": f"{gid}-cai",
                    "kind": "ai",
                    "body": AI_BY_GOAL[gid],
                    "createdAt": created_at,
                }
                comments.insert(pos, ai_comment)
                inserted += 1

    with open(PATH, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write("\n")

    missing = set(AI_BY_GOAL) - {
        g.get("id")
        for r in data for it in r.get("items", []) for g in it.get("subItems", [])
    }
    print(f"inserted={inserted} skipped(existing)={skipped} unmatched_ids={sorted(missing)}")


if __name__ == "__main__":
    main()
